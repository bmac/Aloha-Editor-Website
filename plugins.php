			<h3>Annotations</h3>
			Heavy work in progress...<br />
			With semantic annotaions the user should need less time to work with content and should be enabled to produce content in a higher quality. Additionally it should be possible to annotate content transparently for better automatic processing by other systems.
			<ul>
<li>The Annotations Plugin provides an API to implement one or more annotation services.</li>
<li>The Annotations Plugin provides an API to access annotations for plugins.</li>
<li>The Annotations Plugin provides an API for inline Annotators to apply annotations to the content (RDFa, FOAF, etc.)</li>
<li>Plugins may provide possibilities for manual corrections. They need handling for each entity or may depend on the data the annotation service delivers.</li>
<li>Plugins may use the annotations API from the Annotations Plugin to provide additional informations like autotagging, additional information about an entity, etc.</li>
<li>Special plugins may provide possibility to correct or define special entities eg. http://www.freebase.com/schema/type/people/person</li>
<li>auto-tagging may add additional tags in a defined field around the content</li>
<li>auto-info may display information about an entity when the cursor is in that entity</li>
<li>an information enhancer may provide additional information when the cursor is in an certain entity</li>
<li>a highlighter may mark visually all annotated entities</li>
			</ul>
			
