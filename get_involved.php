				<h2>What is Aloha Editor?</h2>
				<p>Aloha Editor is a semantic Rich Text Editor framework written in Javascript with full
				support of xHTML5. You can integrate it in a CMS, blog, wiki software or any other project
				where you need to edit content with a web based tool.</p>
				
				<h2>What Aloha Editor NOT is!</h2>
				<p>Aloha Editor is NOT an HTML Editor to install on a desktop.</p>
				
				<h2>Why the HTML5 Editor?</h2>
				<p>Aloha Editor implements the full HTML5 specification. It aims to generate full 
				compliant xHTML5 content. Further more the Aloha Editor uses and extends the attribute
				contenteditable which was introduced in the HTML standard with version 5. Most current
				browsers support that attribute but their implementation is inconsitent due to erroneous 
				implementations and the lack of detail of the HTML5 specification.</p>
				
				<h2>The Aloha Spirit</h2>
				<p>The Aloha Editor is an Editor which allows you to edit anywhere. It moves the user
				to the center of everything. Every action should be completed with the minimum 
				interactions as possible. Everything that can be done automatically should be done 
				automatically. All interactions should react in realtime and give immediate clear feedback
				to the user. The user should see the exact result of its action immediately. No apply
				or confirm action should be necessary. It's done when you do it. Instead everthing can
				be reverted.</p>

				<h2>Why Aloha?</h2>
				<p>"Clemens and I started the project on January 18th. We wanted to build the world's 
				most advanced editor and I wanted a codename for the project. So we looked into wikipedia and
				I found out that James Cook discovered Hawaii on January 18th. So I asked Clemens: What 
				would the Hawaiians have said to James Cook? - The Aloha Editor was born" - Haymo Meran</p>
				
